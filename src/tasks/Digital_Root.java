/*Цифровой корень - это рекурсивная сумма всех цифр в числе.
Входным значением будет неотрицательное целое число.
Если это значение содержит более одной цифры, продолжайте уменьшать таким образом,
пока не будет получено однозначное число.*/
/*  16  -->  1 + 6 = 7
   942  -->  9 + 4 + 2 = 15  -->  1 + 5 = 6
132189  -->  1 + 3 + 2 + 1 + 8 + 9 = 24  -->  2 + 4 = 6*/

public class Digital_Root {
    public static int digital_root(int n) {
        return (n - 1) % 9 + 1;
    }
}
